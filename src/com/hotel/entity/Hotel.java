package com.hotel.entity;

public class Hotel {

	private int h_id;
	private String h_name;
	private String h_pic;
	private int style_id;
	private String addr;
	private String totalStorey;
	private String totalRoom;
	private String h_intro;
	private String h_phone;
	public int getH_id() {
		return h_id;
	}
	public void setH_id(int h_id) {
		this.h_id = h_id;
	}
	public String getH_name() {
		return h_name;
	}
	public void setH_name(String h_name) {
		this.h_name = h_name;
	}
	public String getH_pic() {
		return h_pic;
	}
	public void setH_pic(String h_pic) {
		this.h_pic = h_pic;
	}
	public int getStyle_id() {
		return style_id;
	}
	public void setStyle_id(int style_id) {
		this.style_id = style_id;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getTotalStorey() {
		return totalStorey;
	}
	public void setTotalStorey(String totalStorey) {
		this.totalStorey = totalStorey;
	}
	public String getTotalRoom() {
		return totalRoom;
	}
	public void setTotalRoom(String totalRoom) {
		this.totalRoom = totalRoom;
	}
	public String getH_intro() {
		return h_intro;
	}
	public void setH_intro(String h_intro) {
		this.h_intro = h_intro;
	}
	public String getH_phone() {
		return h_phone;
	}
	public void setH_phone(String h_phone) {
		this.h_phone = h_phone;
	}
	
	
}
