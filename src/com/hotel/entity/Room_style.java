package com.hotel.entity;

public class Room_style {

	private int r_style_id;
	private String r_name;
	private float r_area;
	private float price;
	public int getR_style_id() {
		return r_style_id;
	}
	public void setR_style_id(int r_style_id) {
		this.r_style_id = r_style_id;
	}
	public String getR_name() {
		return r_name;
	}
	public void setR_name(String r_name) {
		this.r_name = r_name;
	}
	public float getR_area() {
		return r_area;
	}
	public void setR_area(float r_area) {
		this.r_area = r_area;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	
	
}
